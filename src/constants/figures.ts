import { env } from '../utils/env'

/**
 * 黑色圆圈符号 - 根据平台自动选择最佳显示效果
 *
 * 在 macOS 上使用 ⏺ 符号，具有更好的垂直对齐效果；
 * 在 Windows/Linux 上使用 ● 符号，确保通用兼容性。
 *
 * @example
 * ```typescript
 * // 在不同平台显示状态指示符
 * console.log(`${BLACK_CIRCLE} 正在处理中...`);
 * // macOS: ⏺ 正在处理中...
 * // Windows/Linux: ● 正在处理中...
 * ```
 */
export const BLACK_CIRCLE = env.platform === 'macos' ? '⏺' : '●'
